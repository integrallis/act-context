"""exp-015: de-circularized probe re-grade (find/act boundary) — staged runner.

Re-measures the paper's headline probe claim ("summaries cannot substitute for source when acting")
with the audit's D1/D2 defects removed:
  D1 circularity  -> probes minted from each class's TEST FILE (independent behavioral ground
                     truth; the answerer never sees the tests), with no "answerable only from this
                     source" instruction; answerer may reply UNKNOWN.
  D1 self-judging -> the answering model (Claude Sonnet 4.6) is graded by an INDEPENDENT judge
                     from a different family (OpenAI), with Claude as second judge to report
                     inter-judge agreement (Cohen's kappa).
  D1 own repos    -> held-out classes from seaborn / pylint / pytest at pinned commits.
  D2 summarizer   -> H5 control: the same summary prompt+budget generated by BOTH the original
                     qwen2.5-coder:3b (local) and Claude Sonnet 4.6 (frontier upper bound).

Arms (what the answerer sees): source | summary_qwen3b | summary_frontier | signature.
Registered readout: per-arm accuracy + Wilson CIs per judge; exact McNemar of each arm vs source;
Cohen's kappa between judges. All temperatures 0.

Stages (each persists JSON; later stages refuse to run without prerequisites):
  plan -> mint (Claude, ~15 calls) -> represent (local + ~15 Claude calls) ->
  answer (Claude, n_probes x 4 calls) -> grade (OpenAI + Claude, 2x that) -> analyze (free)
"""
from __future__ import annotations

import argparse
import json
import re
import sys
from datetime import datetime, timezone
from pathlib import Path

HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(HERE))
sys.path.insert(0, str(HERE.parent / "resolve"))
sys.path.insert(0, str(HERE.parents[1] / "src"))

RESULTS = HERE / "results"
ARMS = ["source", "summary_qwen3b", "summary_frontier", "signature"]
K_PROBES = 3
SUMMARY_TOK = 180          # same class-level budget as the original H2 protocol
CHEAP_MODEL = "qwen2.5-coder:3b"
JUDGE_OPENAI_MODEL = "gpt-4o-mini"

_MINT = """Below is the TEST code that exercises the class `{name}` from the `{repo}` repository.

TEST CODE:
{tests}

Write exactly {k} specific, factual questions about the OBSERVABLE BEHAVIOR of `{name}` that this
test code demonstrates, each with a short reference answer grounded in the tests. Ask about the
class's behavior directly (do not mention the tests or test functions in the question). Avoid
questions whose answer is just a name; ask what happens / what is returned / how inputs are treated.

Return STRICT JSON only — a list of {k} objects: [{{"question": "...", "reference": "..."}}, ...]"""

_ANSWER = """Context — a representation of `{name}` from the `{repo}` repository:

{representation}

Question: {question}

Answer in one or two sentences using ONLY the context above. If the context is insufficient to
answer, reply exactly: UNKNOWN"""

_GRADE = """Question about a piece of software: {question}
Reference answer: {reference}
Candidate answer: {answer}

Does the candidate answer agree with the reference answer on the substantive facts? An UNKNOWN,
empty, or factually divergent candidate is INCORRECT. Reply with exactly one word: CORRECT or
INCORRECT."""


def _load(name):
    p = RESULTS / name
    if not p.exists():
        sys.exit(f"REFUSING: missing {p.name}; run the earlier stage first.")
    return json.loads(p.read_text())


def _save(name, obj):
    RESULTS.mkdir(parents=True, exist_ok=True)
    (RESULTS / name).write_text(json.dumps(obj, indent=2))


def _nodes():
    spec = json.loads((HERE / "regrade_nodes.json").read_text())
    for r in spec["repos"]:
        for n in r["nodes"]:
            yield r, n


def _checkout_repo(r):
    from scaled_run import _checkout
    return _checkout(r["repo"], r["base_commit"])


def _parse_probe_json(raw: str, k: int) -> list[dict]:
    m = re.search(r"\[.*\]", raw or "", re.S)
    items = json.loads(m.group(0)) if m else []
    out = [{"question": str(x["question"]).strip(), "reference": str(x["reference"]).strip()}
           for x in items if x.get("question") and x.get("reference")]
    return out[:k]


def stage_mint():
    from dhcm_ng.llm import frontier_client
    fr = frontier_client()
    probes = []
    for r, n in _nodes():
        rp = _checkout_repo(r)
        tests = (rp / n["test_file"]).read_text(encoding="utf-8", errors="ignore")[:24000]
        raw = fr.generate(_MINT.format(name=n["name"], repo=r["repo"], tests=tests, k=K_PROBES),
                          max_tokens=900, temperature=0.0)
        ps = _parse_probe_json(raw, K_PROBES)
        for i, p in enumerate(ps):
            probes.append({"repo": r["repo"], "node": n["id"], "name": n["name"],
                           "probe_id": f"{n['id']}#{i}", **p})
        print(f"minted {len(ps)}/{K_PROBES} for {r['repo']}:{n['name']}", flush=True)
    _save("probes.json", {"minted_at_utc": datetime.now(timezone.utc).isoformat(),
                          "minter": fr.model, "k": K_PROBES, "probes": probes})
    print(f"{len(probes)} probes total")


def stage_represent():
    import tiktoken
    _enc = tiktoken.get_encoding("cl100k_base")
    ntok = lambda t: len(_enc.encode(t or ""))
    from dhcm_ng.extract import extract_tree
    from dhcm_ng.llm import cheap_client, frontier_client
    from dhcm_ng.reduce import uml_class_skeleton
    from dhcm_ng.summarize import _children_digest, _cumulative_prompt, _leaf_summary

    fr = frontier_client()
    ch = cheap_client(CHEAP_MODEL)
    reps = {}
    for r, n in _nodes():
        rp = _checkout_repo(r)
        tree = extract_tree(rp)
        node = tree.nodes[n["id"]]
        lines = (rp / n["path"]).read_text(encoding="utf-8", errors="ignore").split("\n")
        source = "\n".join(lines[n["start_line"] - 1:n["end_line"]])
        # original-protocol class summary input: children digest over deterministic leaf summaries
        for c in tree.children(n["id"]):
            s = _leaf_summary(c)
            c.cumulative_summary = s
            c.residual_summary = s
        digest = _children_digest(tree, node, "cumulative")
        prompt = _cumulative_prompt(node, digest)
        reps[n["id"]] = {
            "source": source,
            "summary_qwen3b": (ch.generate(prompt, max_tokens=SUMMARY_TOK, temperature=0.0) or "").strip(),
            "summary_frontier": (fr.generate(prompt, max_tokens=SUMMARY_TOK, temperature=0.0) or "").strip(),
            "signature": uml_class_skeleton(node.source),
        }
        print(f"represented {r['repo']}:{n['name']} " +
              " ".join(f"{a}={ntok(reps[n['id']][a])}t" for a in ARMS), flush=True)
    tokens = {nid: {a: ntok(v[a]) for a in ARMS} for nid, v in reps.items()}
    _save("representations.json", {"summary_budget_tokens": SUMMARY_TOK,
                                   "cheap_model": CHEAP_MODEL, "tokens_cl100k": tokens,
                                   "representations": reps})


def stage_answer():
    from dhcm_ng.llm import frontier_client
    fr = frontier_client()
    probes = _load("probes.json")["probes"]
    reps = _load("representations.json")["representations"]
    answers = []
    for i, p in enumerate(probes, 1):
        for arm in ARMS:
            a = fr.generate(_ANSWER.format(name=p["name"], repo=p["repo"],
                                           representation=reps[p["node"]][arm],
                                           question=p["question"]),
                            max_tokens=200, temperature=0.0)
            answers.append({"probe_id": p["probe_id"], "arm": arm, "answer": (a or "").strip()})
        print(f"[{i}/{len(probes)}] answered x{len(ARMS)}", flush=True)
    _save("answers.json", {"answerer": fr.model, "answers": answers})


def stage_grade():
    from judge_openai import OpenAIJudge
    from dhcm_ng.llm import frontier_client
    judges = {"openai": OpenAIJudge(JUDGE_OPENAI_MODEL),
              "claude": frontier_client()}
    probes = {p["probe_id"]: p for p in _load("probes.json")["probes"]}
    answers = _load("answers.json")["answers"]
    grades = []
    for i, a in enumerate(answers, 1):
        p = probes[a["probe_id"]]
        row = {"probe_id": a["probe_id"], "arm": a["arm"]}
        for jname, j in judges.items():
            v = (j.generate(_GRADE.format(question=p["question"], reference=p["reference"],
                                          answer=a["answer"] or "(empty)"),
                            max_tokens=8, temperature=0.0) or "").strip().upper()
            row[jname] = bool(re.fullmatch(r"\W*CORRECT\W*", v))
        grades.append(row)
        if i % 20 == 0:
            print(f"[{i}/{len(answers)}] graded", flush=True)
    _save("grades.json", {"judge_openai_model": JUDGE_OPENAI_MODEL, "grades": grades})


def stage_analyze():
    from stats import mcnemar_exact, wilson
    from stats_kappa import cohen_kappa
    grades = _load("grades.json")["grades"]
    toks = _load("representations.json")["tokens_cl100k"]
    by = {a: [g for g in grades if g["arm"] == a] for a in ARMS}
    n = len(by["source"])
    lines = [f"# exp-015 de-circularized probe re-grade (n={n} probes x {len(ARMS)} arms; "
             f"held-out repos; independent judge)\n"]
    out = {"n_probes": n, "arms": {}, "kappa": None, "mcnemar_vs_source": {}}

    for jname in ("openai", "claude"):
        lines.append(f"## Judge: {jname}" + (" (independent family)" if jname == "openai" else
                                             " (same family as answerer — agreement check only)"))
        lines.append("| arm | correct | accuracy | Wilson 95% CI | mean repr tokens |")
        lines.append("|---|---|---|---|---|")
        for a in ARMS:
            k = sum(g[jname] for g in by[a])
            lo, hi = wilson(k, n)
            mt = round(sum(t[a] for t in toks.values()) / len(toks), 1)
            out["arms"].setdefault(a, {})[jname] = {"correct": k, "n": n,
                                                    "ci": [round(lo, 3), round(hi, 3)]}
            lines.append(f"| {a} | {k}/{n} | {k/n:.3f} | [{lo:.2f}, {hi:.2f}] | {mt} |")
        lines.append("")

    src = {g["probe_id"]: g["openai"] for g in by["source"]}
    lines.append("## McNemar vs source (independent judge)\n")
    lines.append("| arm | arm-only | source-only | p |")
    lines.append("|---|---|---|---|")
    for a in ARMS[1:]:
        b = sum(1 for g in by[a] if g["openai"] and not src[g["probe_id"]])
        c = sum(1 for g in by[a] if not g["openai"] and src[g["probe_id"]])
        p = mcnemar_exact(b, c)
        out["mcnemar_vs_source"][a] = {"b": b, "c": c, "p": round(p, 4)}
        lines.append(f"| {a} | {b} | {c} | {p:.4f} |")

    pairs = [(g["openai"], g["claude"]) for g in grades]
    kap = cohen_kappa(pairs)
    agree = sum(1 for x, y in pairs if x == y) / len(pairs)
    out["kappa"] = round(kap, 4)
    lines.append(f"\n**Inter-judge agreement:** {agree:.3f} raw; Cohen's kappa = {kap:.3f} "
                 f"(openai vs claude, {len(pairs)} gradings).")
    _save("analysis.json", out)
    (RESULTS / "ANALYSIS.md").write_text("\n".join(lines) + "\n")
    print("\n".join(lines))


def stage_plan():
    spec = json.loads((HERE / "regrade_nodes.json").read_text())
    nn = sum(len(r["nodes"]) for r in spec["repos"])
    print("exp-015 PLAN (nothing runs):")
    print(f"  nodes: {nn} held-out classes across {[r['repo'] for r in spec['repos']]}")
    print(f"  probes: {nn} x {K_PROBES} minted from TEST files (Claude, T=0)")
    print(f"  arms: {ARMS} (summary budget {SUMMARY_TOK} tok; qwen local + frontier control)")
    print(f"  answer: Claude T=0 -> {nn*K_PROBES*len(ARMS)} calls")
    print(f"  grade: OpenAI {JUDGE_OPENAI_MODEL} + Claude -> {nn*K_PROBES*len(ARMS)*2} cheap calls")
    print("  analyze: Wilson + McNemar-vs-source + Cohen's kappa (free)")


if __name__ == "__main__":
    p = argparse.ArgumentParser()
    p.add_argument("--stage", choices=["plan", "mint", "represent", "answer", "grade", "analyze"],
                   default="plan")
    a = p.parse_args()
    {"plan": stage_plan, "mint": stage_mint, "represent": stage_represent,
     "answer": stage_answer, "grade": stage_grade, "analyze": stage_analyze}[a.stage]()
